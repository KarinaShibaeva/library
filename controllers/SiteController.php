<?php

namespace app\controllers;

use app\models\Book;
use app\models\Category;
use app\models\News;
use app\models\RegisterForm;
use Yii;
use yii\filters\AccessControl;
use yii\web\Controller;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;

class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::class,
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::class,
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        $this->layout = 'layout-fluid';
        $this->getView()->registerCssFile('@web/css/main.css');
        $posts = News::find()->orderBy('date desc')->limit(2)->all();
        $books = Book::find()->orderBy('date_add desc')->limit(5)->all();
        return $this->render('index', ['posts'=>$posts, 'books'=>$books]);
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    public function actionRegister()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new RegisterForm();
        if ($model->load(Yii::$app->request->post()) && $model->register()) {
            return $this->goHome();
        }

        return $this->render('register', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }

    /**
     * Displays about page.
     *
     * @return string
     */
    public function actionAbout()
    {
        $books = Book::find()->orderBy('date_add desc')->limit(5)->all();
        $categories = Category::find()->all();
        return $this->render('about', ['books'=>$books, 'categories'=>$categories]);
    }
    public function actionBook()
    {
        if (isset($_GET['id']) && $_GET['id']!="")
        {
            $categories = Category::find()->where(['id'=>$_GET['id']])->asArray()->one();

            $books = Book::find()->where(['genre_id'=>$_GET['id']])->all();

            //$sect = Section::find()->where(['id'=>$_GET['id']])->asArray()->all();
            //return $this->render('section', compact('categories', 'sections'));

            return $this->render('book', [
                'books' => $books,
                //'sect' => $sect,
                'categories' => $categories,
            ]);
        }
        else
            return $this->redirect(['site/about']);
    }
}
